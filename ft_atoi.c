#include "libft.h"

int	ft_atoi(char	*str)
{
	int	i;
	int	s;
	int	res;

	i = 0;
	s = 1;
	res = 0;
	while (str[i] <= 32 || str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		s *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10;
		res += str[i] - '0';
		i++;
	}
	return (s * res);
}
