#include "libft.h"

int	ft_isalpha(char	*str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] <= 'z' && str[i] >= 'a')
			|| (str[i] <= 'Z' && str[i] >= 'A'))
			i++;
		else
			return (0);
	}
	return (1);
}
