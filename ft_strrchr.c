#include "libft.h"
#include <string.h>

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = strlen(str) - 1;
	if (str == NULL)
		return (NULL);
	while (str[i])
	{
		if (str[i] == (char)c)
			return ((char *)&str[i]);
		i--;
	}
	if (str[i] == (char)c)
		return ((char *)&str[i]);
	return (NULL);
}
