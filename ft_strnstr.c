#include "libft.h"

char	*ft_strnstr(char	*s1, char	*s2, size_t	n)
{
	int		i;
	size_t	j;

	i = 0;
	if (s2[i] == '\0')
		return (s1);
	while (s1[i] && i < n && n)
	{
		j = 0;
		while (s1[i + j] == s2[j] && s1[i + j])
		{
			if (s2[j + 1] == '\0')
				return (&s1[i]);
			j++;
		}
		i++;
	}
	return (0);
}
